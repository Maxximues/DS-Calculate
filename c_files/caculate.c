#include<stdio.h>
#include<math.h>
int main(){
	double D1[10] = {9.012,8.240,7.972,7.769,7.571,7.359,7.138,6.968,6.744,6.509};
	double D2[10] = {6.252,5.762,5.762,5.448,5.122,4.909,4.590,4.225,3.853,3.433};
	double DD[10], R[10], SR[10];
	int i;
	double S = 0, P = 0, Si, A = 0, Yps;
	for(i = 0; i < 10; i++){
		R[i] = (D1[i]*D1[i] - D2[i]*D2[i])/(4*10*589.3*0.000001);
		S = S + R[i];
		printf("R[%d]:%.1f\n",i,R[i]);
	}
	S = S/10;
	printf("S:%.2f\n", S);//S is average banjin
	for(i = 0; i <10; i++){
		P = P + R[i]*R[i];
	}
	printf("P: %.1f\n", P);
	Si = sqrt((P - 10*S*S)/9);
	printf("Si: %.3f\n",Si);
	for(i = 0; i < 10; i++){
		DD[i] = (0.006*sqrt(D1[i]*D1[i] + D2[i]*D2[i]) / (2*10*589.3*0.000001));
		A = A + DD[i];
	}
	A = A/10;
	printf("Uj: %.3f\n", A);
	Yps = sqrt(Si*Si + A*A);
	printf("Yps: %.3f\n", Yps);
	return 0;
}

